/**
 * ------------------------------------------------------------------------
 * Helper Functions
 * ------------------------------------------------------------------------
 */

function generateCharacters(input) {
  var output = "";
  for (var counter = 1; counter <= input; counter += 1) {
    output += "👍";
  }
  return output;
}

/**
 * ------------------------------------------------------------------------
 * Main
 * ------------------------------------------------------------------------
 */

function main(input) {
  return generateCharacters(input);
}
