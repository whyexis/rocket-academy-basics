function main(input) {
  var base = "misc.";
  if (
    input == "chicken rice" ||
    input == "nasi lemak" ||
    input == "bak kut teh"
  ) {
    base = "rice";
  }
  if (input == "hokkien mee" || input == "laksa") {
    base = "noodle";
  }
  return `The dish ${input} has a ${base} base.`;
}
